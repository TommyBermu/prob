### Función de densidad ###
###########################

"una función f se denomina función de densidad de probabilidad si f(x)
cumple:

  i) f(x) >=0
  ii) ∫(-∞ -> ∞) f(x) dx
  iii) P(a <= X <= b) = ∫(a -> b) f(x) dx
  
el área bajo la curva de una función de masa es su probabilidad
"

